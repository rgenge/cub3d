#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	int		counter;
	size_t	i;

	counter = 0;
	i = 0;
	if (*src == 0)
		dest[0] = '\0';
	while (src[counter++] != '\0')
		;
	if (size != 0)
	{
		while (src[i] != '\0' && i < (size - 1))
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (counter - 1);
}
